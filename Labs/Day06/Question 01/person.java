public class person {
    private String name;
    private int age;

    public person(String name, int age) {
        name = "Navitha";
        age = 19;
    }
    
    //setters
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    //getters
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    

}